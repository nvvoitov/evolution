package com.evolution.area;

public class Area {
	private int width;
	private int heigh;

	public Area (int width, int heigh){
		this.width = width;
		this.heigh = heigh;
	}
	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeigh() {
		return heigh;
	}

	public void setHeigh(int heigh) {
		this.heigh = heigh;
	}

}
